package jungol.문자열2;

import java.util.Scanner;

// 미완
public class 문자열2_형성평가4 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        String[] S = new String[n];

        for (int i = 0; i < n; i++) {
            String str = sc.nextLine();
            S[i] = str;
        }

    }
}
