package java_basic_01.day2;

public class VariableEx01 {
    public static void main(String[] args) {
        // 1. 변수 value 할당
        int value;
        // 2. 변수 value 20 할당
        value = 20;
        // 3. 변수 result에 value + 30 후 저장
        int result = value + 30;
        // 4. 변수 result 값을 콘솔 출력
        System.out.println(result);
    }


}
